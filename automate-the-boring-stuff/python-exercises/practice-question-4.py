'''
Write a short program that prints the number 1 to 10 using a for 
loop. Then, write an equivalent program that prints the numbers 1 to 10 
using a while loop.
''' 
#for loop
print('\nprint 1 to 10 using for loop')
for i in range(1,11): print(i)



#while loop
print('\nprint 1 to 10 using while loop')
a = 1
while a < 11:
    print(a)
    a+=1
    